package main

import "fmt"

func main() {
	fmt.Println(9)              // integer
	fmt.Println(9 + 3)          // integer addition
	fmt.Println(9.5)            // float
	fmt.Println(9.5 / 3.0)      //	 float division
	fmt.Println("Hello Golang") // string
	fmt.Println(true)           // boolean
	fmt.Println(false)          // boolean
}
